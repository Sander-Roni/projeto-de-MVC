    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String email = jTextField6.getText().trim();
        String telefone = jTextField7.getText().trim();
        String senha = jTextField8.getText().trim();
        String idade = jTextField9.getText().trim();
        String cpf = jTextField10.getText().trim();
        chamadoCliente.setEmail(email);
        chamadoCliente.setTelefone(telefone);
        chamadoCliente.setSenha(senha);
        chamadoCliente.setIdade(idade);
        chamadoCliente.setCpf(cpf);
        try{
            dao.AlterarCliente(chamadoCliente);
            JOptionPane.showMessageDialog(null, "Registros Atualizados com sucesso");
        }catch (SQLException ex) {
            java.util.logging.Logger.getLogger(AlterarConta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao atualizar: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(AlterarConta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao encontrar a classe necess√°ria: " + ex.getMessage());
        }
        
        TelaCliente tp = new TelaCliente();
        tp.setVisible(rootPaneCheckingEnabled);
    }//GEN-LAST:event_jButton3ActionPerformed

